	<h1>How We Met</h1>
	<p>Toward the beginning of February I was talking with one of my best
		friends who is engaged to a long-time friend of Molly's. During this
		conversation I told him that all I wanted was to meet a NICE girl, a
		girl who was kind and polite. He said you should meet Molly. At first,
		I wasn't up to the idea of being introduced to someone, I wanted to
		find them on my own. I went on with my life and forgot about the
		conversation completely.</p>
	<p>A couple weeks later we were talking, and he again told me I should
		meet Molly. Again, I hesitated at the thought of being set up with
		someone. However, this time the thought stayed in my head. It stayed
		in my head, and I started thinking to myself, what if she is really
		nice, what if she is someone for me? After a few days of thinking
		about it I called him and told him I wanted to meet her.</p>
	<p>Thoughts were racing through my head. Would I like her? Is she going
		to like me? What am I supposed to talk to her about? I worried about
		how I would tell her I wasn't interested in her if she wasn't what I
		was looking for.</p>
	<p>March 16th finally rolled around. I can't say that I was very
		focused that day in class. I even thought about writing down things in
		advance to talk about. Dinner time finally arrived, and it was time to
		meet her.</p>
	<p>When I first saw her, I introduced myself, and said something I
		thought was funny. She laughed and I thought she might just understand
		me. From the very start I was intrigued by her, I thought she was
		beautiful before she even said a word. I thought to myself, I'm going
		to try as hard as I can to win her over.</p>
	<p>We had dinner, talked, and had a couple laughs. After dinner we hung
		out a while longer. I told her I was interested in getting to know her
		and asked her if she felt the same. I was pleasantly surprised when
		she said yes. I asked for her phone number, she gave it to me, and the
		rest is history.</p>